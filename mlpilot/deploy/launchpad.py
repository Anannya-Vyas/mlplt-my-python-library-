from __future__ import annotations

import os
import pickle
from pathlib import Path
from typing import Any, Optional

import pandas as pd

from mlpilot.utils.display import print_step, print_success, print_warning


_FASTAPI_MAIN_TEMPLATE = '''"""
Auto-generated FastAPI prediction API — mlpilot LaunchPad
Model: {model_name} v{version}
"""

import pickle
from pathlib import Path
from typing import Dict, List, Any

from fastapi import FastAPI, HTTPException
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel

app = FastAPI(
    title="{model_name} Prediction API",
    description="Auto-generated by mlpilot LaunchPad",
    version="{version}",
)

app.add_middleware(
    CORSMiddleware, allow_origins=["*"], allow_methods=["*"], allow_headers=["*"]
)

# Load model
MODEL_PATH = Path(__file__).parent / "model.pkl"
with open(MODEL_PATH, "rb") as f:
    MODEL = pickle.load(f)

FEATURE_NAMES = {feature_names!r}


class PredictionRequest(BaseModel):
    features: Dict[str, Any]


class PredictionResponse(BaseModel):
    prediction: Any
    confidence: str


class BatchRequest(BaseModel):
    rows: List[Dict[str, Any]]


@app.get("/health")
def health():
    return {{"status": "healthy", "model": "{model_name}", "version": "{version}"}}


@app.get("/model-info")
def model_info():
    return {{
        "model_name": "{model_name}",
        "version": "{version}",
        "feature_names": FEATURE_NAMES,
        "n_features": len(FEATURE_NAMES),
    }}


@app.post("/predict", response_model=PredictionResponse)
def predict(request: PredictionRequest):
    import pandas as pd
    try:
        row = pd.DataFrame([request.features])
        prediction = MODEL.predict(row)[0]
        confidence = "high"
        if hasattr(MODEL, "predict_proba"):
            proba = MODEL.predict_proba(row)[0]
            max_prob = float(max(proba))
            confidence = "high" if max_prob > 0.8 else "medium" if max_prob > 0.6 else "low"
        return PredictionResponse(prediction=str(prediction), confidence=confidence)
    except Exception as e:
        raise HTTPException(status_code=400, detail=str(e))


@app.post("/predict/batch")
def predict_batch(request: BatchRequest):
    import pandas as pd
    try:
        df = pd.DataFrame(request.rows)
        predictions = MODEL.predict(df).tolist()
        return {{"predictions": predictions, "count": len(predictions)}}
    except Exception as e:
        raise HTTPException(status_code=400, detail=str(e))


@app.post("/feedback")
def feedback(prediction_id: str, actual_label: Any):
    # Log prediction feedback for drift monitoring
    return {{"status": "logged", "prediction_id": prediction_id}}


if __name__ == "__main__":
    import uvicorn
    uvicorn.run(app, host="0.0.0.0", port=8000)
'''

